# We will learn how to define our own functions in this program. A function is a block of code that performs a specific task. We can call the function whenever we want to perform that task, which helps us avoid repeating code and makes our programs easier to read and maintain.


# define a function
def my_function():
    print("Hello, I am a function!")


# call the function
my_function()

# We can also define functions that take parameters, which are values that we can pass to the function to customize its behavior. For example, let's define a function that takes a name as a parameter and prints a greeting:


def greet(name):
    print(f"Hello, {name}!")


# call the function with a parameter
greet("Alice")

